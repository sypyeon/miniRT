#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	size_t	str_len;
	char	*new_str;

	if (!(s || f))
		return (0);
	str_len = ft_strlen(s);
	new_str = (char *)malloc(sizeof(char) * str_len + 1);
	if (!new_str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		new_str[i] = f(i, s[i]);
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
